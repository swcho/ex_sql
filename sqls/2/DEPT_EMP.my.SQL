CREATE TABLE DEPT (
  DEPTNO INT(2),
  DNAME VARCHAR(14),
  LOC   VARCHAR(13),
  CONSTRAINT PK_DEPT PRIMARY KEY (DEPTNO) ) ;

CREATE TABLE EMP (
  EMPNO INT(4),
  ENAME VARCHAR(10),
  JOB   VARCHAR(9),
  MGR   INT(4),
  HIREDATE DATE,
  SAL   decimal(7,2),
  COMM  decimal(7,2),
  DEPTNO INT(2),
  CONSTRAINT PK_EMP PRIMARY KEY (EMPNO),
  CONSTRAINT FK_DEPTNO FOREIGN KEY (DEPTNO) REFERENCES DEPT (DEPTNO));

